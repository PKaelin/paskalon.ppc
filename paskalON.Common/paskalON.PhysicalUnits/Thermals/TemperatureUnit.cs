namespace paskalON.PhysicalUnits.Thermals
{
    /// <summary>
    /// Primary temperature scales used in the world.
    /// </summary>
    public enum TemperatureUnit
    {
        /// <summary>
        /// Used by the scientific community and nearly every country globally.
        /// </summary>
        /// <remarks>
        /// Celsius: Water freezes at 0 degrees and boils at 100 degrees.
        /// </remarks>
        Celsius = 0,
        /// <summary>
        /// Used primarily in the United States and a few Caribbean territories.
        /// </summary>
        /// <remarks>
        /// Fahrenheit: Water freezes at 32 fahrenheit and boils at 212 fahrenheit.
        /// </remarks>
        Fahrenheit = 1
    }
}
